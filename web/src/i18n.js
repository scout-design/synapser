// Reactive i18n
import { reactive } from 'vue'
const state = reactive({
  locale: localStorage.getItem('synapse_locale') || 'en'
})

export const i18n = {
  get locale() { return state.locale },
  setLocale(locale) { state.locale = locale; localStorage.setItem('synapse_locale', locale) },
  t(key) {
    const keys = key.split('.')
    let value = messages[state.locale]
    for (const k of keys) { value = value?.[k] }
    return value || key
  }
}
